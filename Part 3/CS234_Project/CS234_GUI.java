    /**
     * This method handles updating the appointment panel as well as updating the appointment comboboxes throughout
     * the rest of the program.
     */
    private void updateAppointmentPanel(){
        AppointmentCatalogue.setText("");
        for(Appointments appointment:Appointments.getAppointmentsList()){
            AppointmentCatalogue.append("Client: " + appointment.getClient().getClientName() + 
                                                            "\n Employee: " + appointment.getEmployee() +
                                                            "\n Date: " + appointment.getDate() + "\n----------------------\n");
        }
        
        RemoveAppointmentBox.removeAllItems();
        for(Appointments appointment:Appointments.getAppointmentsList()){
             RemoveAppointmentBox.addItem("Client: "  + appointment.getClient().getClientName() +  " Employee: " + appointment.getEmployee().getEmployeeName());
        }
        
        UpdateAppointmentBox.removeAllItems();
        for(Appointments appointment:Appointments.getAppointmentsList()){
             UpdateAppointmentBox.addItem("Client: "  + appointment.getClient().getClientName() +  " Employee: " + appointment.getEmployee().getEmployeeName());
        }
        
        DefaultTableModel model = (DefaultTableModel) AppointmentTable.getModel();
        model.setRowCount(0);
        for(Appointments appointment: Appointments.getAppointmentsList()){
            String employee = appointment.getEmployee().getEmployeeName();
            String client = appointment.getClient().getClientName();
            String date = appointment.getDate();
           
            
            String data[] = {client,employee,date};
            
            model.addRow(data);     
        }

        CS234_Project.Main.writeToFile();
    }
    
    
