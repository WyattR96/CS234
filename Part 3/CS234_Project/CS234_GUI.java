    /**
     * This method handles updating the employee catalogue any time an employee is added, removed or updated. it also
     * handles updating the employee comboboxes throughout the program.
     */
    private void updateEmployeePanel(){
        RemoveEmployeeBox.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            RemoveEmployeeBox.addItem(employee.getEmployeeName());
        }
        
        UpdateEmployeeBox.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            UpdateEmployeeBox.addItem(employee.getEmployeeName());
        }
        
        EmployeeCatalogue.setText("");
        for(Employees employee: Employees.getEmployeeList()){
            EmployeeCatalogue.append("Name: " + employee.getEmployeeName() + " | Salary: "+ employee.getEmployeeSalary() + "\n");
        }
        
        AddServiceEmployee.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            AddServiceEmployee.addItem(employee.getEmployeeName());
        }
        
        UpdateServiceEmployee.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            UpdateServiceEmployee.addItem(employee.getEmployeeName());
        }
        
        UpdateAppointmentEmployee.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            UpdateAppointmentEmployee.addItem(employee.getEmployeeName());
        }
        
        EmployeeAppointmentBox.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            EmployeeAppointmentBox.addItem(employee.getEmployeeName());
        }
        
        DefaultTableModel model = (DefaultTableModel) EmployeeTable.getModel();
        model.setRowCount(0);
        for(Employees employee: Employees.getEmployeeList()){
            String name= employee.getEmployeeName();
            String salary = String.valueOf(employee.getEmployeeSalary());
            
            String data[] = {name,salary};
            
            model.addRow(data);     
        }

        CS234_Project.Main.writeToFile();
    }
    
