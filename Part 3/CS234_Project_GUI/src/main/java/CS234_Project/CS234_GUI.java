    private void UpdateEmployeeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateEmployeeButtonActionPerformed
        int employeeIndex = UpdateEmployeeBox.getSelectedIndex();
        Employees employeeUpdate = Employees.getEmployeeList().get(employeeIndex);
        double newSalary = Double.parseDouble(NewSalary.getText());
        employeeUpdate.setEmployeeSalary(newSalary);
        
        NewSalary.setText("");
        
        updateEmployeePanel();
    }//GEN-LAST:event_UpdateEmployeeButtonActionPerformed
    
    private void updateEmployeePanel(){
        RemoveEmployeeBox.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            RemoveEmployeeBox.addItem(employee.getEmployeeName());
        }
        
        UpdateEmployeeBox.removeAllItems();
        for(Employees employee:Employees.getEmployeeList()){
            UpdateEmployeeBox.addItem(employee.getEmployeeName());
        }
        
        EmployeeCatalogue.setText("");
        for(Employees employee: Employees.getEmployeeList()){
            EmployeeCatalogue.append("Name: " + employee.getEmployeeName() + " | Salary: "+ employee.getEmployeeSalary() + "\n");
        }
    }
    
