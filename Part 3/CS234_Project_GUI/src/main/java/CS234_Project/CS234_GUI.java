    private void DeleteCarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteCarButtonActionPerformed
        int carIndex = DeleteCarBox.getSelectedIndex();
        Cars removeCar = Cars.getCarsList().get(carIndex);
        
        Cars.getCarsList().remove(removeCar);
        
        CarCatalogue.setText("");
        for(Cars car: Cars.getCarsList()){
            CarCatalogue.append("Make: " + car.getMake() + " Model: " + car.getModel() + "\n");
        }
        
        DeleteCarBox.removeAllItems();
        for(Cars car: Cars.getCarsList()){
             DeleteCarBox.addItem("Make: " + car.getMake() +" Model: " + car.getModel());
        }
        
        updateCarsPanel();
    }//GEN-LAST:event_DeleteCarButtonActionPerformed

