    private void RefreshServiceCatalogueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RefreshServiceCatalogueActionPerformed
        ServiceCatalogue.setText("");
        for(Services service: Services.getServicesList()){
            
            String employee = service.getEmployee().getEmployeeName();
            String client = service.getClient().getClientName();
            String problem = service.getCarService();
            double cost = service.getServiceCost();
            
            ServiceCatalogue.append("Employee: " + employee + "\n" +
                                                   "Client: " + client + "\n" +
                                                   "Problem:" + "\n" + problem + "\n" +
                                                   "Cost: " + cost + "\n" +
                                                   "---------------------------------------" + "\n");
        }
    }//GEN-LAST:event_RefreshServiceCatalogueActionPerformed
    
    
    
