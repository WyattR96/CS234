    private void UpdateCarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateCarButtonActionPerformed
        int carIndex = UpdateCarBox.getSelectedIndex();
        Cars car = Cars.getCarsList().get(carIndex);
        
        if(UpdateMakeField.getText().isEmpty()){
            car.setModel(UpdateModelFeild.getText());
        }else if(UpdateModelFeild.getText().isEmpty()){
            car.setMake(UpdateMakeField.getText());
        }
        
        updateCarsPanel();
    }//GEN-LAST:event_UpdateCarButtonActionPerformed
    
    private void updateCarsPanel(){
        CarCatalogue.setText("");
        for(Cars car: Cars.getCarsList()){
            CarCatalogue.append("Make: " + car.getMake() + " Model: " + car.getModel() + "\n");
        }
        
        DeleteCarBox.removeAllItems();
        for(Cars car: Cars.getCarsList()){
             DeleteCarBox.addItem("Make: " + car.getMake() +" Model: " + car.getModel());
        }
        
        UpdateCarBox.removeAllItems();
        for(Cars car: Cars.getCarsList()){
             UpdateCarBox.addItem("Make: " + car.getMake() +" Model: " + car.getModel());
        }
        
        UpdateClientVehicle.removeAllItems();
        for(Cars car: Cars.getCarsList()){
             UpdateClientVehicle.addItem("Make: " + car.getMake() +" Model: " + car.getModel());
        }
        
        AddClientVehicle.removeAllItems();
        for(Cars car: Cars.getCarsList()){
             AddClientVehicle.addItem("Make: " + car.getMake() +" Model: " + car.getModel());
        }
    }
    
